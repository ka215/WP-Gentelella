// Convert serialized array ( eq. $.serializeArray() ) to key values type
function conv_kv( _array ) {
  var kv_object = {};
  for ( idx in _array ) {
    var key   = _array[idx]['name'];
    var value = _array[idx]['value'];
    kv_object[key] = value;
  }
  return kv_object;
}
